import { defineCollection, z } from 'astro:content';

const posts = defineCollection({
  type: 'content',
  schema: z.object({
    title: z.string(),
    date: z.string(),
    description: z.string().optional(),
    investorOnly: z.boolean().default(false),
    draft: z.boolean().default(false),
  }),
});

export const collections = {
  posts,
};
